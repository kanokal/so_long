#include "../includes/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	idx;

	if (s == NULL)
		return (NULL);
	idx = 0;
	while (idx < n)
	{
		if (*(unsigned char *)(s + idx) == (unsigned char)c)
			return ((void *)(s + idx));
		idx++;
	}
	return (NULL);
}
