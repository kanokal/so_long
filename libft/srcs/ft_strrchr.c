#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		idx;

	if (s == NULL)
		return (NULL);
	idx = (int)ft_strlen(s);
	while (idx >= 0)
	{
		if (s[idx] == (char)c)
			return ((char *)(s + idx));
		idx--;
	}
	return (NULL);
}
