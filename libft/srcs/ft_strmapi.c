#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*temp;
	unsigned int	idx;

	if ((temp = ft_strdup(s)) == NULL)
		return (NULL);
	idx = 0;
	while (temp[idx])
	{
		temp[idx] = (*f)(idx, temp[idx]);
		idx++;
	}
	return (temp);
}
