#include "../includes/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			idx;
	unsigned char	*temp;

	if (b == NULL)
		return (NULL);
	idx = 0;
	temp = (unsigned char *)b;
	while (idx < len)
	{
		*(temp + idx) = (unsigned char)c;
		idx++;
	}
	return (b);
}
