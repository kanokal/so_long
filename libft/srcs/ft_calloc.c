#include "../includes/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	if ((ptr = (void *)malloc(count * size)) == NULL)
		return (NULL);
	ft_bzero(ptr, count);
	return (ptr);
}
