#include "../includes/libft.h"

void	ft_strcpy(char *dst, char *src)
{
	int	idx;

	idx = 0;
	while (dst[idx] != 0 && src[idx] != 0)
	{
		dst[idx] = src[idx];
		idx++;
	}
}
