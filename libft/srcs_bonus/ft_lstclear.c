#include "../includes/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (lst == NULL || lst[0] == NULL || del == NULL)
		return ;
	temp = lst[0];
	while (temp)
	{
		temp = temp->next;
		(*del)(lst[0]->content);
		free(lst[0]);
		lst[0] = temp;
	}
}
